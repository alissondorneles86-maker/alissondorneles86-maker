# alert.sh

## Functionality
Checks disk usage and logs alerts if usage exceeds the defined threshold (80%).  
Logs include the usage percentage and timestamp for clear auditing.

## Purpose
Learning exercise focusing on:
- Bash scripting
- Linux system monitoring
- Automated alerting
- Blue Team fundamentals

## Usage
```bash
# Run the disk usage alert check
./alert.sh

# View alert log
cat alerts.log